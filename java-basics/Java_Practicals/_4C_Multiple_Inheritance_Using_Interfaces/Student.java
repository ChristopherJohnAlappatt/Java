package Java_Practicals._4C_Multiple_Inheritance_Using_Interfaces;

public class Student {
    
    int Roll_No;
    String Name;

    public int getRoll_No() {
        return Roll_No;
    }

    public void setRoll_No(int roll_No) {
        Roll_No = roll_No;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

}
